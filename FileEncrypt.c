struct arguments{
	int is_enc;
	int is_dec;
	const char *in_file;
	const char *out_file;
};

void usage(const char *cmd);
void parse_cmdline(int argc, char *argv[], struct arguments *p_args);
off_t filesize(const char *filename);

int main(int argc, char *argv[])
{
	int ret = EXIT_SUCCESS;
	struct arguments st = {0, 0, NULL, NULL};
	off_t f_size;
	FILE *in, *out;
	unsigned char buf[BUFFER_SIZE] = {0};
	size_t r_size, w_size;
	int i, key;
	register int offset;

	parse_cmdline(argc, argv, &st);
	f_size = filesize(st.in_file);
	offset = st.is_enc ? (f_size % MAP_SIZE) : MAP_SIZE - (f_size % MAP_SIZE);

	if((in = fopen(st.in_file, "rb")) == NULL)
	{
		perror(st.in_file);
		return EXIT_FAILURE;
	}
	if((out = fopen(st.out_file, "wb")) == NULL)
	{
		perror(st.out_file);
		fclose(in);
		return EXIT_FAILURE;
	}

	while(!feof(in))
	{
		r_size = fread(buf, 1, BUFFER_SIZE, in);
		if(ferror(in))
		{
			ret = EXIT_FAILURE;
			perror(st.in_file);
			break;
		}

		if(r_size)
		{
			if(st.is_enc)
			{
				for(i = 0; i < r_size; i++)
				{
					key = (buf[i] + offset) % MAP_SIZE;
					buf[i] = map[key];
				}
			}
			else
			{
				for(i = 0; i < r_size; i++)
				{
					key = buf[i];
					buf[i] = (map_rev[key] + offset) % MAP_SIZE;
				}
			}

			w_size = fwrite(buf, 1, r_size, out);
			if(w_size != r_size || ferror(out))
			{
				ret = EXIT_FAILURE;
				perror(st.out_file);
				break;
			}
		}
	}

	fclose(out);
	fclose(in);

	return ret;
}

void usage(const char *cmd)
{
	fprintf(stderr, "Usage\n\tEncrypt: %s -e -i <input file> -o <output file>\n\tDecrypt: %s -d -i <input file> -o <output file>\n", cmd, cmd);
	exit(EXIT_FAILURE);
}

void parse_cmdline(int argc, char *argv[], struct arguments *p_args)
{
	int ch;
	while((ch = getopt(argc, argv, "edi:o:")) != -1)
	{
		switch(ch)
		{
			case 'e':
				p_args->is_enc = 1;
				break;
			case 'd':
				p_args->is_dec = 1;
				break;
			case 'i':
				p_args->in_file = optarg;
				break;
			case 'o':
				p_args->out_file = optarg;
				break;
		}
	}
	if(p_args->is_enc == p_args->is_dec || !p_args->in_file || !p_args->out_file)
	{
		usage(argv[0]);
	}
}

off_t filesize(const char *filename)
{
	struct stat st = {};
	if(stat(filename, &st))
	{
		perror(filename);
		exit(EXIT_FAILURE);
	}
	return st.st_size;
}